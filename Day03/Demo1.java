class Demo1 {
    public static void main(String[] args) {
        boolean ret = 10 > 5;
        System.out.println(ret);

        ret = 5 >= 5;
        System.out.println(ret);

        ret = 10 < 5;
        System.out.println(ret);

        ret = 10 <= 5;
        System.out.println(ret);

        ret = 10 == 5;
        System.out.println(ret);

        ret = 10 != 5;
        System.out.println(ret);
    }
}